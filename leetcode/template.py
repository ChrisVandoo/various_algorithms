TESTS = [
    {
        "input": "",
        "expected": "",
    },
    {
        "input": "",
        "expected": "",
    }
]

def solution(input):
    return input

def test():
    for test in TESTS:
        result = solution(test["input"])
        if result == test.get("expected"):
            print ("SUCESS! {} == {}".format(result, test.get("expected")))
        else:
            print("FAIL. {} != {}".format(result, test.get("expected")))

if __name__ == "__main__":
    test()